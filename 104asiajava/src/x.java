import java.util.*;
public class x {

	/**
	 * @param args
	 */

	public static void main(String[] args) { 
		int a[]={10,20,30,40,50}; //創造陣列a 
		for(int i=a.length;i>0;i--){ 
		//i=陣列a的最大元素數,如果a>0的話,會執行i-1迴圈 
		System.out.println(a[i-1]); 
		//因為length是取得元素數,必須把她-1才會等於陣列的索引值,否則會多1造成執行錯誤 
		} 
		} 
		}


